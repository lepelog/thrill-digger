#[derive(Clone)]
pub struct RngContext {
    state: u32,
}

impl RngContext {

    pub fn new() -> RngContext {
        RngContext {
            state: 0
        }
    }

    pub fn from_state(state: u32) -> RngContext {
        RngContext {
            state: state,
        }
    }

    #[inline]
    pub fn next_u32(&mut self) -> u32 {
        let temp1: u64 = self.state as u64 * 1664525u64 + 1013904223u64;
        self.state = (((temp1 >> 0x20) + temp1) & 0xFFFFFFFF) as u32;
        self.state
    }

    pub fn next_float(&mut self) -> f32 {
        // copied from skyward sword
        f32::from_be_bytes((self.next_u32() >> 9 | 0x3f800000).to_be_bytes()) - 1f32
    }

    pub fn rng_call_distance(start: u32, end: u32) -> u32 {
        let mut rng_ctx = RngContext::from_state(start);
        let mut distance: u32 = 0;
        while rng_ctx.next_u32() != end {
            distance+=1;
        }
        distance
    }

    #[inline]
    pub fn set_state(&mut self, new_state: u32) {
        self.state = new_state;
    }

    #[inline]
    pub fn get_state(&self) -> u32 {
        self.state
    }
}

